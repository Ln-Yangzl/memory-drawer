    public static List<Point> drawText(int x, int y, String text) {
        List<Point> points = new ArrayList<>();
        for (int i = 0; i < text.length(); i++) {
            points.addAll(drawChar(x + i * CHAR_SIZE, y, text.charAt(i)));
        }
        return points;
    }

    public static List<Point> drawChar(int x, int y, char c){
        ArrayList<Point> points = new ArrayList<>();
        char[] current = N_ASCII_DOT[c - ' '];
        for (int i = 0; i < CHAR_SIZE; i++) {
            char line = current[i];
            for (int j = 0; j < CHAR_SIZE; j++) {
                if(((line >>> (CHAR_SIZE - 1 - j)) & 1) == 1){
                    points.add(new Point(x + j, y + (CHAR_SIZE - 1 - i)));
                }
            }
        }
        return points;
    }

